import pandas as pd
import numpy as np

# ---------------------------------------------------------
# 1. 금융 데이터 생성 (DataFrame 초기화)
# ---------------------------------------------------------
# 날짜와 종목별 가격, 거래량을 포함한 가상의 포트폴리오 데이터입니다.
data = {
    'Date': pd.to_datetime(['2024-04-15', '2024-04-16', '2024-04-17', '2024-04-18', '2024-04-19']),
    'Samsung': [72000, 71500, 73000, 72500, 74000],
    'SK_Hynix': [180000, 178000, 182000, 185000, 190000],
    'Volume': [1500000, 1200000, 1800000, 1100000, 2000000]
}

df = pd.DataFrame(data)
# 날짜를 인덱스로 설정하면 시계열 분석이 더 편해집니다.
df.set_index('Date', inplace=True)

print("--- [1] 기초 데이터프레임 ---")
print(df)
print("\n")

# ---------------------------------------------------------
# 2. 파생 변수 생성 (수익률 및 이동평균)
# ---------------------------------------------------------
# (1) 일일 수익률 계산: (오늘 가격 / 어제 가격) - 1
df['Samsung_Ret'] = df['Samsung'].pct_change() * 100

# (2) 가격 변동폭 (diff)
df['Hynix_Diff'] = df['SK_Hynix'].diff()

# (3) 3일 이동평균 (Moving Average)
df['Samsung_MA3'] = df['Samsung'].rolling(window=3).mean()

print("--- [2] 수익률 및 이동평균 추가 ---")
print(df[['Samsung', 'Samsung_Ret', 'Samsung_MA3']])
print("\n")

# ---------------------------------------------------------
# 3. 데이터 필터링 및 조건 검색
# ---------------------------------------------------------
# 삼성전자 수익률이 양수(+)이면서 하이닉스 가격이 18만 원 이상인 날 찾기
condition = (df['Samsung_Ret'] > 0) & (df['SK_Hynix'] >= 180000)
filtered_df = df[condition]

print("--- [3] 조건 필터링 결과 (상승장 & 하이닉스 18만↑) ---")
print(filtered_df)
print("\n")

# ---------------------------------------------------------
# 4. 결측치(NaN) 처리
# ---------------------------------------------------------
# 첫 번째 날은 이전 데이터가 없어 수익률이 NaN으로 나옵니다.
print("--- [4] 결측치 확인 및 처리 ---")
print(f"NaN 개수:\n{df.isna().sum()}")

# NaN을 0으로 채우거나(fillna), 해당 행을 삭제(dropna)할 수 있습니다.
df_filled = df.fillna(0)
print("\n[결측치를 0으로 채운 후]")
print(df_filled.head(2))
print("\n")

# ---------------------------------------------------------
# 5. 통계 요약 (금융 데이터 성격 파악)
# ---------------------------------------------------------
print("--- [5] 포트폴리오 요약 통계 ---")
# 종목별 평균, 변동성(std), 최솟값, 최댓값을 한눈에 봅니다.
print(df[['Samsung', 'SK_Hynix']].describe())

